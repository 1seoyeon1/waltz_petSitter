package com.pet.sitter.vo;

public class BlogCategories {

	private String lifestyle;
	private String myPet;
	private String knowledge;
	private String playground;
	private String petItems;
	public String getLifestyle() {
		return lifestyle;
	}
	public void setLifestyle(String lifestyle) {
		this.lifestyle = lifestyle;
	}
	public String getMyPet() {
		return myPet;
	}
	public void setMyPet(String myPet) {
		this.myPet = myPet;
	}
	public String getKnowledge() {
		return knowledge;
	}
	public void setKnowledge(String knowledge) {
		this.knowledge = knowledge;
	}
	public String getPlayground() {
		return playground;
	}
	public void setPlayground(String playground) {
		this.playground = playground;
	}
	public String getPetItems() {
		return petItems;
	}
	public void setPetItems(String petItems) {
		this.petItems = petItems;
	}
}
